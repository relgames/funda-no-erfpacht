package org.relgames.funda.parser;

/**
 * @author opoleshuk
 */
public class House {
    private String url;
    private String address;
    private String erfpacht;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getErfpacht() {
        return erfpacht;
    }

    public void setErfpacht(String erfpacht) {
        this.erfpacht = erfpacht;
    }
}
